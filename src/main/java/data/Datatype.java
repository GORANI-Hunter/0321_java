package data;

public class Datatype {
    public static  void main (String[] args){

        System.out.println(6); // number
        System.out.println("six") ; //String 문자열
        System.out.println("6"); //String 6
        System.out.println(6+6); // 12 숫자 더하기 현산
        System.out.println("6"+"6"); //문자열 6 6 이더해져 =66
        System.out.println(6*6); //36
        // System.out.println("6"*"6"); 데이터 타입이 불가
        System.out.println("1111".length()); // 4 문자열의 길이
        //System.out.println(1111.length()); 데이터 타입 연산 불가

        //데이터 타입을 구분하는 이유와 숫와 문자열을 표햔하는 방법 정리

    }
}
