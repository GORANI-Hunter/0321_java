package data;

public class Number {
    public static void main(String[] args) {
        System.out.println(6 + 2); //8
        System.out.println(6 - 2); //4
        System.out.println(6 * 2); //12
        System.out.println(6 / 2); //3
        // 연산자 Operator

        System.out.println(Math.PI); //3.141592653589793
        System.out.println(Math.floor(Math.PI)); //소수점 컷
        System.out.println(Math.ceil(Math.PI)); //0.1 이상 강제 반올림
        System.out.println(Math.pow(2, 6)); //pow 2의 6 제곱의 값 구하기
        //수학적 작업 처리가능 나중에 자바로 계산기 만들기 목표
    }
}
